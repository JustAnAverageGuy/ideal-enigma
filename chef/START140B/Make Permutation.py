import sys;input=sys.stdin.readline

def solve():
    n = int(input())
    A = list(map(int,input().strip().split()))
    A.sort()
    for i in range(1,n+1):
        if A[i-1] > i: return False
    return True
    

for _ in range(int(input())):print("YES" if solve() else "NO")

