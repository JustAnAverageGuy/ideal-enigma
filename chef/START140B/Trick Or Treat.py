import sys;input=sys.stdin.readline

def solve():
    n,m = map(int,input().strip().split())
    A = list(map(int,input().strip().split()))
    B = list(map(int,input().strip().split()))
    ma = [0]*m
    for i in A:
        ma[(-i)%m] += 1
    c = 0
    for i in B:
        c += ma[i%m]
    print(c)
    

for _ in range(int(input())): solve()

