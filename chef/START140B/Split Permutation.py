import sys;input=sys.stdin.readline

def solve():
    n = int(input())
    if n == 1:
        print(1)
        return
    if n == 2:
        print(1,2)
        return
    if n == 3:
        print(1,2,3)
        return
    for i in range(1, n//2+1):
        print(i, n-(n%2)+1-i,end=' ')
    if n%2 == 0:
        print('')
        return
    print(n)
    


for _ in range(int(input())): solve()

