import sys;input=sys.stdin.readline

def solve():
    n,x,y = map(int,input().strip().split())
    print(max(
        map(
            lambda cx : cx*x+ ((n-cx)//2)*y, 
            range(n+1)
        )
    ))
    

for _ in range(int(input())): solve()

