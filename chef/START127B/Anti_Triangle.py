import sys;input=sys.stdin.readline

def solve():
    n,l = map(int,input().strip().split())
    if (n)*l <= 10**9:
        print(*range(l,(n+1)*l,l))
        return
    print(*range(1,n+1))

for _ in range(int(input())): solve()

