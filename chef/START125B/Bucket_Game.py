import sys;input=sys.stdin.readline

def solve():
    n = int(input())
    A = list(map(int,input().strip().split()))
    
    if n == 1: return A[0] % 2


    par = [0,0]
    for i in A: par[i%2] += 1
    if par[1] == 0:
        return 1
    if par[1] == 1:
        return 0
    if n == 2:
        a,b = sorted(A)
        if a == 1 and (b % 2 == 1): return 2
        return (a-b + 1) % 2
    # return 2

    

for _ in range(int(input())): print(["Alice","Bob","Draw"][solve()])

