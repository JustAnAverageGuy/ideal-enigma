import sys;input=sys.stdin.readline

def solve():
    n = int(input())
    A = list(map(int,input().strip().split()))
    all1 = (1<<32)-1
    while True:
        curror = 0
        for i in A: curror |= i

        # print(bin(curror)[2:],file=sys.stderr )

        if curror & (curror + 1) == 0:
            print(n - len(A))
            return

        notmask = all1 ^ (curror ^ (curror + 1))
        A = [i for i in A if (notmask & i) == 0 ]
        # curror = 0
        # for i in filtered: curror |= i
        # print(filtered,f'{curror:05b}', file=sys.stderr)

for _ in range(int(input())): solve()

