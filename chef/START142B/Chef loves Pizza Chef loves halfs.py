import sys;input=sys.stdin.readline

def solve():
    n = int(input())
    x = 1
    if n&(n-1) == 0:
        print(0)
        return
    while x <= n: x <<= 1
    x >>= 1
    rem = n - x
    print(2*rem)
    # print(rem, file=sys.stderr)

for _ in range(int(input())): solve()

