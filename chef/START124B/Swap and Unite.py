# def longest_contiguous_and_remaining_chrs

from collections import defaultdict
def solve():
    s = input().strip()
    ctr = defaultdict(int)

    ans = float('inf')

    for i in s: ctr[i] += 1
    # print(ctr, file=sys.stderr)

    for c in ctr:
        # print(f'Checking "{c}":',file=sys.stderr)
        ln = ctr[c]
        diff = sum(i!=c for  i in s[:ln])
        ans = min(ans, diff)
        for r in range(ln, len(s)):
            diff += (s[r] != c) - (s[r+1-ln-1] != c)
            ans = min(ans, diff)
    print(ans)
    




    
    

for _ in range(int(input())): solve()

