from io import BytesIO, IOBase
import os
import sys

BUFSIZE = 64


class FastIO(IOBase):
    newlines = 0

    def __init__(self, file):
        self._fd = file.fileno()
        self.buffer = BytesIO()
        self.writable = "x" in file.mode or "r" not in file.mode
        self.write = self.buffer.write if self.writable else None

    def read(self):
        while True:
            b = os.read(self._fd, max(os.fstat(self._fd).st_size, BUFSIZE))
            if not b:
                break
            ptr = self.buffer.tell()
            self.buffer.seek(0, 2), self.buffer.write(b), self.buffer.seek(ptr)
        self.newlines = 0
        return self.buffer.read()

    def readline(self):
        while self.newlines == 0:
            b = os.read(self._fd, max(os.fstat(self._fd).st_size, BUFSIZE))
            self.newlines = b.count(b"\n") + (not b)
            ptr = self.buffer.tell()
            self.buffer.seek(0, 2), self.buffer.write(b), self.buffer.seek(ptr)
        self.newlines -= 1
        return self.buffer.readline()

    def flush(self):
        if self.writable:
            os.write(self._fd, self.buffer.getvalue())
            self.buffer.truncate(0), self.buffer.seek(0)


class IOWrapper(IOBase):
    def __init__(self, file):
        self.buffer = FastIO(file)
        self.flush = self.buffer.flush
        self.writable = self.buffer.writable
        self.write = lambda s: self.buffer.write(s.encode("ascii"))
        self.read = lambda: self.buffer.read().decode("ascii")
        self.readline = lambda: self.buffer.readline().decode("ascii")


sys.stdin, sys.stdout = IOWrapper(sys.stdin), IOWrapper(sys.stdout)
def input(): return sys.stdin.readline().rstrip('\r\n')



from collections import defaultdict

def solve():
    n,q = map(int,input().strip().split())
    tree = defaultdict(list)
    for i in range(n-1): # O(n)
        u,v = map(int,input().strip().split())
        tree[u].append(v)
        tree[v].append(u)
    children = defaultdict(list)
    depths = [-1]*(n+1) # O(n)
    depths[1] = 0
    dfs = [(1,0)]

    state = [0]*(n+1)
    
    while dfs:
        node, parent = dfs.pop()
        depths[node] = depths[parent] + 1

        for i in tree[node]:
            if i != parent:
                children[node].append(i)
                dfs.append((i, node))
    # print(f'{children = }\n{depths = }\n{depthwise_wrt_root = }', file=sys.stderr)

    for _ in range(q):
        t, node = map(int,input().strip().split())
        if t == 2:
            state[node] = 1 - state[node]
        else:
            # assuming all queries of type 1
            # assert node == 1
            if node == 1:
                sum_depths = defaultdict(int) 
                for nodes in range(2,n+1):
                    sum_depths[depths[nodes]] += state[nodes]
                print(sum(i == 1 for i in sum_depths.values()))








for _ in range(int(input())): solve()

