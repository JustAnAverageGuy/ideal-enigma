import sys;input=sys.stdin.readline

def solve():
    n,k = map(int,input().strip().split())
    A = list(map(int,input().strip().split()))
    mins = float('inf')
    for i in A:
        if i < k: continue
        mins = min(mins, i%k)
    if mins == float('inf'):
        print(-1)
    else:
        print(mins)
    

for _ in range(int(input())): solve()

