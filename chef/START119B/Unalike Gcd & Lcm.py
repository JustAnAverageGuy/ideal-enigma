import sys;input=sys.stdin.readline

from collections import defaultdict


def prime_factors(n):
    t = n
    if n == 0: return defaultdict(int)
    f = defaultdict(int)
    if not(t&1):
        while not(t&1):
            f[2] += 1
            t >>= 1
    i = 3
    while i * i <= t:
        if t % i == 0:
            while t % i == 0:
                f[i] += 1
                t //= i
        i += 2
    if t>1: f[t] += 1
    return dict(f)

MOD = int(1e9+7)

def solve():
    x,q = map(int,input().strip().split())
    f = prime_factors(x)
    for _ in range(q):
        p = int(input())
        c = 1
        if p == 1: print(1); continue
        
        for pi in f:
            if f[pi] % p == 0: c *= 2
        print(c)

        

    

for _ in range(int(input())): solve()

