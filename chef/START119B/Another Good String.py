import sys;input=sys.stdin.readline

def solve():
    n,q = map(int,input().strip().split())
    s = input().strip()
    
    last = ""
    le = 0
    max_le = 0

    for i in range(n):
        if s[i] == last:
            le += 1
        else:
            last = s[i]
            max_le = max(max_le, le)
            le = 1
    max_le = max(max_le, le)

    print(max_le, end=' ')
    for i in range(q):
        c = input().strip()
        if c == last:
            le += 1
        else:
            last = c
            le = 1
        max_le = max(max_le, le)
        print(max_le,end=' ')
    print()

    
    

for _ in range(int(input())): solve()

