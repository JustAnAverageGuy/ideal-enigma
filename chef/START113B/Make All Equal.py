import sys

input = sys.stdin.readline


def solve():
    n, m = map(int, input().strip().split())
    A = list(map(int, input().strip().split()))
    k = max(A)
    print((n * k - sum(A) + m - 1) // m)


for _ in range(int(input())):
    solve()
