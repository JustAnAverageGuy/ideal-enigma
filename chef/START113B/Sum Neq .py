import sys

input = sys.stdin.readline


def solve():
    n = int(input())
    A = list(map(int, input().strip().split()))
    if n == 4: return A[0] + A[1] != A[2] + A[3]
    return len(set(A)) > 1


for _ in range(int(input())):
    print("YES" if solve() else "NO")
