from re import L
import sys;input=sys.stdin.readline

def solve():
    n = int(input())
    A = list(map(int,input().strip().split()))
    if n == 1:
        print(A[0])
        return
    currand = max(A) 
    B = []

    while A:
        curmax = max(i&currand for i in A) 
        newa = []
        for j in A:
            if j & currand == curmax:
                B.append(curmax)
            else:
                newa.append(j)
        currand = curmax
        A = newa[:]
    print(*B)







for _ in range(int(input())): solve()

