import sys;input=sys.stdin.readline

def solve():
    n,x = map(int,input().strip().split())
    if x == 0: print(*range(1,n+1)); return  
    if x >= n-1: 
        print(-1)
        return
    print(*range(n,x+1,-1), *range(1,x+2))

for _ in range(int(input())): solve()




















