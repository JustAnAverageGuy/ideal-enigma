s = input()
from collections import defaultdict
d = defaultdict(int)

for i in s: d[i] += 1

hmm = defaultdict(int) 
for j in d.values():
    hmm[j] += 1

print("Yes" if all(k in [0,2] for k in hmm.values()) else "No")

