def givlsb(n):
    if n == 0: return 1 << 60
    j = 0
    while ((n >> j)&1) == 0: j += 1
    return (1 << j)

L,R = map(int,input().strip().split())
stages = [L]

l = L
while l != R:
    lsb = givlsb(l)
    while True:
        if l + lsb <= R:
            l += lsb
            stages.append(l)
            break
        else:
            lsb >>= 1

print(len(stages) - 1)
for i in range(len(stages) - 1): print(*stages[i:i+2])
