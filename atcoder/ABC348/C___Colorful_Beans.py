import sys;input=sys.stdin.readline
n = int(input())
from collections import defaultdict


ans = defaultdict(lambda : 1e9+2)

for i in range(n):
    a,c = map(int,input().strip().split())
    ans[c] = min(ans[c], a)
print(max(ans.values()))

