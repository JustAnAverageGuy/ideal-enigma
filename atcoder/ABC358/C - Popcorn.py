import sys;input=sys.stdin.readline

n,m = map(int,input().strip().split())
A = []
for i in range(n):
    s = input()
    A.append(set(i for i in range(m) if s[i] == 'o'))

minans = n

for mask in range(1<<n):
    seen = set()
    for b in range(n):
        if (mask >> b) & 1: seen.update(A[b])
    if len(seen) == m: minans = min(minans, mask.bit_count())
print(minans)

