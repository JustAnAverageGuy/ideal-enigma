import sys;input=sys.stdin.readline

def solve():
    n,m = map(int,input().strip().split())
    if m == 0:
        print(n)
        return
    l = max(n-m, 0)
    r = n + m
    # for each bit, check if there exist a number between l and r such that it has that bit set 
    ans = 0
    for b in range(32):
        if (l>>b)&1:
            ans |= (1<<b) 
            continue
        nx =  (l & ((1<<32) - (1<<b))) | (1 << b) 
        if nx <=r: ans |= (1<<b)
    print(ans)
    

for _ in range(int(input())): solve()

