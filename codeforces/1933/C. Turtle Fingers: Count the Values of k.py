import sys;input=sys.stdin.readline

def log(base, value):
    x = 1
    cnt = 0
    while x <= value:
        x *= base
        cnt += 1
    return cnt

def solve():
    a,b,l = map(int,input().strip().split())
    maxx = log(a, l) + 0
    maxy = log(b, l) + 0
    possk = set()
    for x in range(maxx):
        for y in range(maxy):
            hmm = (a ** x ) * (b ** y)
            if l % hmm == 0: possk.add(l // hmm)
    print(len(possk))
    # print(maxx, maxy,poss_ks, len(poss_ks), file=sys.stderr)

    

for _ in range(int(input())): solve()

