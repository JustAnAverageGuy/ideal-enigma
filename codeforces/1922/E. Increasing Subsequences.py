import sys;input=sys.stdin.readline

def solve():
    n = int(input())
    if n <= 200:
        print(n)
        print(*([0]*n))
        return
    
    x = bin(n)[2:]
    ns = []
    t = len(x)
    for i in range(t):
        if x[t-1-i] == '1': ns.append(i)
    r = len(ns)
    ans = []
    
    hmm = 10**9
    for j in ns:
        ans.extend(range(hmm - j, hmm))
        hmm -= (j)
    
    ans.extend([-10**9]*r)
    print(len(ans))
    print(*ans)

    
for _ in range(int(input())): solve()




















