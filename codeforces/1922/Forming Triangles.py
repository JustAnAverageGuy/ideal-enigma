import sys;input=sys.stdin.readline

from collections import defaultdict
def solve():
    n = int(input())
    A = list(map(int,input().strip().split()))
    s = defaultdict(int)
    for i in A: s[i] += 1
    cnt = 0
    ans = 0
    for i in sorted(s.keys()):
        x = s[i]
        ans += (((s[i])*(s[i] - 1)) // 2) * cnt + ((s[i])*(s[i] - 1)*(s[i] - 2))//6
        cnt += s[i]
    print( ans)
    

for _ in range(int(input())): solve()




















