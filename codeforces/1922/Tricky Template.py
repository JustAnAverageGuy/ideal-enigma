import sys;input=sys.stdin.readline

def solve():
    n = int(input())
    a = input().strip()
    b = input().strip()
    c = input().strip()
    for i in range(n):
        if a[i] == b[i] != c[i] or a[i] != b[i] != c[i] != a[i]: return True
        
    return False


    
    

for _ in range(int(input())): print("YES" if solve() else "NO")




















