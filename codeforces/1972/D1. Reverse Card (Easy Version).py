import sys;input=sys.stdin.readline

def solve():
    n,m = map(int,input().strip().split())
    s = 0
    for g in range(1,m+1):
        lmmx = n//g
        ful, rem = divmod(lmmx,g)
        s += ful + (rem>0)*(rem == g-1)
    print(s)

    

for _ in range(int(input())): solve()

