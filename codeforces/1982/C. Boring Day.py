import sys;input=sys.stdin.readline

def solve():
    n,l,r = map(int,input().strip().split())
    A = list(map(int,input().strip().split()))

    succes_cnt = 0
    curr_sum = 0

    L = -1
    for R in range(n):
        curr_sum += A[R]
        if curr_sum > r:
            while (curr_sum > r and L < R):
                L += 1
                curr_sum -= A[L]
            if l <= curr_sum <= r:
                succes_cnt += 1
                curr_sum = 0
                L = R
        elif l <= curr_sum <= r:
            succes_cnt += 1
            curr_sum = 0
            L = R

    print(succes_cnt)



    

for _ in range(int(input())): solve()

