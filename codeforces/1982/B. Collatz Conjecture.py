import sys;input=sys.stdin.readline

def solve():
    x,y,k = map(int,input().strip().split())
    # print('--------',file=sys.stderr)
    while k > 0:
        r = (-x)%y
        if r == 0:
            r += y
        # print(x,y,k,r,file=sys.stderr)
        if k - r < 0:
            print(x + k)
            return
        k -= r
        x += r
        while x%y == 0:
            x //= y
        if x == 1:
            k %= (y-1)

    print(x)



    

for _ in range(int(input())): solve()

