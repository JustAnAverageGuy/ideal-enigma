import sys;input=sys.stdin.readline

def solve():
    a = list(input().strip())
    b = list(input().strip())
    n = len(a)
    if len(a) == 1: print(f'{a[0]}\n{b[0]}'); return

    foundiff = False
    for i in range(n):
        if a[i] != b[i]:
            if foundiff:
                b[i],a[i] = sorted([a[i],b[i]])
            else:
                a[i],b[i] = sorted([a[i],b[i]])
                foundiff = True
    print(*a, sep='')
    print(*b, sep='')
                

    

for _ in range(int(input())): solve()

