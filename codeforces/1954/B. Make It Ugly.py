import sys;input=sys.stdin.readline

def solve():
    n = int(input())
    A = list(map(int,input().strip().split()))
    # find minimum number of elements to remove so that the end points are different
    edp = A[0]

    different = []
    for i in range(1,n):
        if A[i] != edp: different.append(i)
    
    if len(different) == 0: print(-1); return

    m = min(different[0], n-1-different[-1])
    for j,k in zip(different, different[1:]):
        m = min(m, k-j-1)
    print(m)

    


for _ in range(int(input())): solve()

