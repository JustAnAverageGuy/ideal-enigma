import sys;input=sys.stdin.readline
for _ in range(int(input())):
    n,m,k = map(int,input().strip().split())
    # divide evenly, php type 
    c = (n + m-1)//m 
    # atleast one color will have this many 
    # rest needs to be converted
    print("YES" if (n - c) > k else "NO")

