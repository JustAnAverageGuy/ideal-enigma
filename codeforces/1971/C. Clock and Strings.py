import sys;input=sys.stdin.readline

def side(a,b,c):

    return (c-a)*(c-b)

def solve():
    a,b,c,d = map(int,input().strip().split())
    a,b =sorted([a,b])
    c,d =sorted([c,d])
    x = side(a,b,c)
    y = side(a,b,d)
    # print(x,y,file=sys.stderr)
    return x*y <= 0
    






