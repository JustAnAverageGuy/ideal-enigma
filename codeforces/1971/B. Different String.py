import sys;input=sys.stdin.readline

def solve():
    s = list(input().strip())
    if s != sorted(s):
        print("YES")
        print(*sorted(s),sep="")
        return
    if s != sorted(s,reverse=True):
        print("YES")
        print(*sorted(s,reverse=True),sep="")
        return
    print("NO")
    

for _ in range(int(input())): solve()

