def a2n(w):
    n = 0
    for l in w:
        d = ord(l) - ord('A')
        n *= 26
        n += d
    return n

def n2a(n):
    digs = []
    while n > 0:
        dig = n % 26
        n //= 26
        digs.append(
            chr(ord('A') + dig)
        )
    if not digs:
        digs = ['A']
    return "".join(digs[::-1])

