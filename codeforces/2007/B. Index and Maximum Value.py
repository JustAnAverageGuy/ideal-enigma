import sys;input=sys.stdin.readline

def solve():
    n,m = map(int,input().strip().split())
    A = list(map(int,input().strip().split()))
    ans = []

    curmax = max(A)

    for _ in range(m):
        s,l,r = input().strip().split()
        l = int(l)
        r = int(r)
        dx = [-1,1][s == '+']
        if r >= curmax >= l:
            curmax += dx
            ans.append(curmax)
            continue
        else:
            ans.append(curmax)
            continue
    print(*ans)


    

for _ in range(int(input())): solve()

