import sys;input=sys.stdin.readline

def ans(n,k):
    if n == 1: return 0 
    if k >= n: return 1
    rem = n%k
    return rem * ans(n//k + 1, k) + (n-rem) * ans(n//k, k)

def solve():
    n,k = map(int,input().strip().split())
    print(ans(n,k))

    

for _ in range(int(input())): solve()

