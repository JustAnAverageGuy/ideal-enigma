import sys;input=sys.stdin.readline

def solve():
    b,c,d = map(int,input().strip().split())
    B,C,D = b,c,d
    a = 0
    i = 1
    while b or c or d:
        bb = b&1
        cc = c&1
        dd = d&1
        if not dd:
            if bb and not cc: print(-1); return
            if bb and cc: a |= i
        else:
            if cc and not bb: print(-1); return
            if not (bb or cc): a |= i; 

        # print(a,b,c,d)
        i <<= 1
        b >>= 1
        c >>= 1
        d >>= 1
    print(a)
    

for _ in range(int(input())): solve()

