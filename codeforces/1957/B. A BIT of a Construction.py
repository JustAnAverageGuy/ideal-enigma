import sys;input=sys.stdin.readline

def solve():
    n,k = map(int,input().strip().split())
    if n == 1: print(k); return
    s = 1
    while s <= k:
        s <<= 1
        s |= 1
    s >>= 1
    # print(s)
    sm = k - s
    a = [s]
    if sm > 0: a.append(sm)
    print(*a,'0 '*(n - len(a)))
    
    

for _ in range(int(input())): solve()

