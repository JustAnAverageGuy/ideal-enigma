dp = [1,1,3]
MOD = int(1e9+7)

for n in range(3,3_00_001):
    dp.append(
        (dp[n-1] + (2*n-2)*dp[n-2])%MOD
    )


import sys;input=sys.stdin.readline
# print(dp[:10],file=sys.stderr)

def solve():
    n,k = map(int,input().strip().split())
    remsqurs = n
    for i in range(k):
        a,b = map(int,input().strip().split())
        remsqurs -= 1 + (a!=b)
    # print(remsqurs, file=sys.stderr)
    print(dp[remsqurs])


    

for _ in range(int(input())): solve()

