import sys;input=sys.stdin.readline

def solve():
    n,k,x = map(int,input().strip().split())
    A = list(map(int,input().strip().split()))
    A.sort()
    pref = [0]
    for i in A: pref.append(pref[-1] + i)
    maxsum = float('-inf')
    for a in range(0, min(n,k) + 1):
        b = min(n-a, x)
        rem = n-a-b
        sm = 2* pref[rem] - (pref[rem+b])
        maxsum = max(maxsum, sm)
    print(maxsum)

for _ in range(int(input())): solve()




















