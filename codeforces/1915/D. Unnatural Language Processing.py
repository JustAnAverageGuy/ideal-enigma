import sys;input=sys.stdin.readline

def solve():
    n = int(input())
    s=input().strip()
    A = []
    for i in s:
        A.append(i)
        A.append("")
    # print(A, file=sys.stderr)
    for i in range(2,len(A)-2,2):
        if A[i] in "bcd" and A[i+2] in "ae": A[i-1] = "."
    print("".join(A))
for _ in range(int(input())): solve()




















