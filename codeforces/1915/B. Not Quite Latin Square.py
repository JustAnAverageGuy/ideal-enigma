import sys;input=sys.stdin.readline

def solve():
    s = [input().strip() for i in range(3)]
    quest = None
    for i in range(3):
        for j in range(3):
            if s[i][j] == '?':
                quest = (i,j)
    chars = set(s[quest[0]])
    print(set("ABC").difference(chars).pop())

for _ in range(int(input())): solve()




















