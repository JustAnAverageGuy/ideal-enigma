import sys;input=sys.stdin.readline

def solve():
    a,b,c = map(int,input().strip().split())
    mn = a + (b//3)
    lefttrip = b%3 
    if lefttrip:
        if c < 3 - lefttrip: print(-1); return
        mn += 1 + (c - 3 + lefttrip + 2)//3
    else:
        mn += (c+2)//3

    print(mn)
    return 
    

for _ in range(int(input())): solve()

