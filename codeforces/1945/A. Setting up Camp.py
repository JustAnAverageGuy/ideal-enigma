import sys;input=sys.stdin.readline

def solve():
    n = int(input())
    s = input().strip()
    correct_left = 0
    correct_right = s.count("1")
    possible = []
    if correct_right >= (n+1): possible.append(0)
    for i in range(1, n+1):
        c = s[i-1]
        if c == '0':
            correct_left += 1
        else:
            correct_right += 1
        if correct_left >= (i+1)//2 and correct_right >= (n-i+1)//2:
            possible.append(i)
    print(possible)



for _ in range(int(input())): solve()

