import sys;input=sys.stdin.readline
from collections import Counter

def solve():
    n,c,d = map(int,input().strip().split())
    A = Counter(map(int,input().strip().split()))
    a = min(A) 
    # print(a, file=sys.stderr)
    for i in range(n):
        for j in range(n):
            if A[a + i*c + j*d] <= 0: return False
            A[a + i*c + j*d]  -= 1
    return True


for _ in range(int(input())): print("YES" if solve() else "NO")

