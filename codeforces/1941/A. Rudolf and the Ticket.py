import sys;input=sys.stdin.readline

def solve():
    n,m,k = map(int,input().strip().split())
    B = list(map(int,input().strip().split()))
    C = list(map(int,input().strip().split()))

    cnt = 0
    for b in B:
        for c in C:
            cnt += (b+c <= k)
    print(cnt)
    

for _ in range(int(input())): solve()

