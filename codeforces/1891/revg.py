def revg(k):
    ranges = []
    for f in range(2, 60):
        # f**k <= x < f**(k+1)
        l = max(f**k, (1 << f))
        r = min(f**(k+1) - 1, (1 << (f+1)) - 1)

        if l <= r:
            ranges.append((l, r))

    return ranges

