import sys;input=sys.stdin.readline

def solve():
    n = int(input())
    A = [input().strip() for i in range(n)]
    ans = 0
    for row in range(0, (n+1)//2):
        for col in range(0,(n+1)//2):
            i = row
            j = col
            chrs = [A[ro][cl] for ro,cl in [(i,j), (j,n-1-i),(n-1-i,n-1-j), (n-1-j,i)]]
            m = max(chrs)
            for i in chrs: ans += ord(m) - ord(i)
    print(ans) 
            
                
                

for _ in range(int(input())): solve()