import sys;input=sys.stdin.readline

def solve():
    n = int(input())
    s = input().strip()
    if s <= s[::-1]:
        if n % 2 == 0:
            print(s)
            return
        else:
            print(s + s[::-1])
            return
    else:
        if n%2 == 0:
            print(s[::-1] + s)
            return
        else:
            print(s[::-1])
            return

for _ in range(int(input())): solve()

