import sys;input=sys.stdin.readline

def solve():
    n,k = map(int,input().strip().split())
    B = list(map(int,input().strip().split()))
    p = 1
    for i in B: p*=i
    if 2023%p: print("NO"); return
    a = 2023//p
    print("YES")
    print(*([1]*(k-1) + [a]))
    

for _ in range(int(input())): solve()




















