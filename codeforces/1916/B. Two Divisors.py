import sys;input=sys.stdin.readline
from math import gcd

def solve():
    a,b = map(int,input().strip().split())
    g = gcd(a,b)
    lcm = (a*b)//g
    if a == 1: print(b*b); return
    if lcm == b:
        b_ = b//g
        print(b_*b_*g)
    else:
        print(lcm)

for _ in range(int(input())): solve()




















