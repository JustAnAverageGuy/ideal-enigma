import sys;input=sys.stdin.readline

def solve():
    n = int(input())
    s = input().strip()
    for i,j in zip(s,s[1:]):
        if i.isalpha() and j.isdigit(): return False
    lett = [i for i in s if i.isdigit()]
    dig = [i for i in s if i.isalpha()]
    return dig == sorted(dig) and lett == sorted(lett)
    

for _ in range(int(input())): print("YES" if solve() else "NO")

