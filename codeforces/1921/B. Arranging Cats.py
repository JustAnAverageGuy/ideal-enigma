import sys;input=sys.stdin.readline

def solve():
    n = int(input())
    s = input().strip()
    f = input().strip()
    changes = [0]
    changes.append(0)
    for i,j in zip(s,f):
        if i!=j:
            if i == '1': changes[1] += 1
            if i == '0': changes[0] += 1
    ans = min(changes)
    changes[0] -= ans
    changes[1] -= ans
    print(ans + max(changes))

for _ in range(int(input())): solve()




















