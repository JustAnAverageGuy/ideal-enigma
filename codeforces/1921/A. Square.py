import sys;input=sys.stdin.readline

def solve():
    A = [tuple(map(int,input().strip().split())) for i in range(4)]
    xs = [i[0] for i in A]
    ys = [i[1] for i in A]
    mix = min(xs)
    ma = max(xs)
    print((ma-mix)**2)
    

for _ in range(int(input())): solve()




















