import sys;input=sys.stdin.readline

def solve():
    n,f,a,b = map(int,input().strip().split())
    B = list(map(int,input().strip().split()))
    A = [0,*B]
    for i in range(n):
        d = A[i+1] - A[i]
        mi = min(d * a , b)
        f -= mi
        if f <= 0: return False
    return True
        
     

for _ in range(int(input())): print("YES" if solve() else "NO")




















