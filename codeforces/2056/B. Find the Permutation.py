import sys;input=sys.stdin.readline

def solve():
    # print("----",file=sys.stderr)
    n = int(input())
    perm = [0]*(n)
    for i in range(1,n+1):
        adj = input().strip()
        pos = i-1
        # print(adj, pos,file=sys.stderr)
        for j in range(1, n+1):
            if j == i: continue
            if j < i:
                if adj[j-1] == '0': pos -= 1
            else:
                if adj[j-1] == '0': pos += 1
            # print(pos, file=sys.stderr)
        # print(pos, file=sys.stderr)
        perm[pos] = i
    print(*perm)


    

for _ in range(int(input())): solve()

