import sys;input=sys.stdin.readline

def solve():
    s = input()
    hr,min = map(int,s.split(":"))
    suff = "AP"[hr >= 12]+"M"
    hr %= 12
    hr += 12 * (hr == 0)
    print(f'{hr:02}:{min:02} {suff}')
    

for _ in range(int(input())): solve()

