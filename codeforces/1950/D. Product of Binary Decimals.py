bdec = {int(bin(i)[2:]) for i in range(1, 32)}


import sys;input=sys.stdin.readline
# print(bdec, file=sys.stderr)

from functools import cache 


@cache 
def isposs(n):
    if n in bdec: return True
    for k in bdec:
        if k == 1: continue
        if n%k == 0:
            if isposs(n // k): return True
    return False

def solve():
    n = int(input())
    if n == 10**5: return True
    return isposs(n)

for _ in range(int(input())): print("YES" if solve() else "NO")

