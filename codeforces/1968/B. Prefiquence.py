import sys;input=sys.stdin.readline


def solve():
    n,m = map(int,input().strip().split())
    a = input().strip()
    b = input().strip()
    l = -1
    r = min(n,m) + 1

    while r-l > 1:
        mid =  l + (r-l)//2
        # print(l,mid,r, file=sys.stderr)

        j = 0
        for i in range(mid):
            while (j<m) and a[i] != b[j]: j += 1
            if j >= m: 
                r = mid
                break
            j += 1
        else:
            l = mid
        # print(l,mid,r, file=sys.stderr)
    # print(l,r)
    print(l)
    # print(f'-----', file=sys.stderr)
        
            

    
    

for _ in range(int(input())): solve()

