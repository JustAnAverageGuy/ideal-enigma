import sys;input=sys.stdin.readline

def solve():
    n = int(input())
    A = list(map(int,input().strip().split()))
    # print('----------------', file=sys.stderr)
    deltas = []
    for i in range(1,n):
        if A[i-1] > A[i]:
            deltas.append(A[i-1] - A[i])
            A[i] = A[i-1]
    # print(A)
    if not deltas:
        print(0)
        return
    tot_sum = sum(deltas)
    deltas.sort()
    # print(deltas, file=sys.stderr)
    # print(tot_sum)
    currsum = 0
    for d in deltas:
        if currsum >= d: continue
        tot_sum += d - currsum
        currsum = d
    print(tot_sum)

    

for _ in range(int(input())): solve()

