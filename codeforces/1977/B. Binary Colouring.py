import sys;input=sys.stdin.readline

def solve():
    n = int(input())
    t = n
    k = 0
    hmm = [0]*32
    while t:
        hmm[k] = t&1
        k += 1
        t >>= 1
    
    for i in range(30):
        if hmm[i] == 2:
            hmm[i] = 0 
            hmm[i+1] += 1
            continue
        if hmm[i] == 1 and hmm[i+1] == 1:
            hmm[i] = -1
            hmm[i+1] = 0
            hmm[i+2] += 1
    print(32)
    print(*hmm)

    

for _ in range(int(input())): solve()

