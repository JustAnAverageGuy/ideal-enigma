import sys;input=sys.stdin.readline

def solve():
    n,m = map(int,input().strip().split())
    s = list(input().rstrip())
    A = sorted(set(list(map(int,input().strip().split()))))
    c = sorted(input().rstrip())[:len(A)]
    for idx, cc in zip(A,c):
        s[idx-1] = cc
    print(*s,sep='')


    

for _ in range(int(input())): solve()

