import sys;input=sys.stdin.readline

def solve():
    n,m = map(int,input().strip().split())
    A = [input().strip() for i in range(n)]
    r,c = -1,-1

    rbegin = -1
    rend = n
    for i in A:
        c = i.find('#')
        rbegin += 1
        if c != -1: break

    for x,i in enumerate(A[rbegin+1:],start=rbegin+1):
        if i.find('#') == -1:
            rend = x
            break
    print((rbegin + rend-1)//2 +1, c+1)

    

for _ in range(int(input())): solve()

