import sys;input=sys.stdin.readline


def solve():
    n = int(input())
    A = list(map(int,input().strip().split()))
    cnt = 0
    seen = set()
    s = 0
    for i in A:
        s += i
        seen.add(str(i))
        if (s&1)==0 and str((s>>1)) in seen: cnt += 1
    # print(seen)
    print(cnt)


    

for _ in range(int(input())): solve()

