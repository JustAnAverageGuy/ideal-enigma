import sys;input=sys.stdin.readline

def solve():
    h,n = map(int,input().strip().split())
    A = list(map(int,input().strip().split()))
    C = list(map(int,input().strip().split()))
    l = 0
    a,c = A[0], C[0]
    natt = (h+a-1)//a 
    r = 1 + (natt-1)*c + 1

    while r-l > 1:
        m = l + (r-l)//2
        s = 0
        for aa, cc in zip(A,C):
            s += ((m-1)//cc+1)*aa
            if s >= h:
                r = m
                break
        else:
            l = m
    print(r)



    

for _ in range(int(input())): solve()

