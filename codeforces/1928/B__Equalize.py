import sys;input=sys.stdin.readline

def solve():
    n = int(input())
    uniq = sorted(set(map(int,input().strip().split())))

    ma = 1
    l,r = 0,0

    while r < len(uniq):
        delta = uniq[r] - uniq[l]
        if delta <= n-1: ma = max(ma, r-l+1)
        if delta > n-1 and l < r:
            l += 1
        else:
            r += 1
    print(ma)

for _ in range(int(input())): solve()

