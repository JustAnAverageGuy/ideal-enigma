import sys;input=sys.stdin.readline

def solve():
    a,b,c = map(int,input().strip().split())
    ans = a*b*c
    for da in range(6):
        for db in range(6):
            for dc in range(6):
                if da+db+dc <= 5:
                    ans = max(ans, (a+da)*(b+db)*(c+dc))
    print(ans)
    

for _ in range(int(input())): solve()

