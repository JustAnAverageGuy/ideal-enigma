import sys;input=sys.stdin.readline
n,q = map(int,input().strip().split())    
A = list(map(int,input().strip().split()))
B = list(map(int,input().strip().split()))
input() # ignore c

sa = sum(A)
sb = sum(B)
for i in range(q):
    p,a,b,_ = map(int,input().strip().split())
    sa += a - A[p-1]
    sb += b - B[p-1]
    A[p-1] = a
    B[p-1] = b
    print(min(sa,sb))


    




















