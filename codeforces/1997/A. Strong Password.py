import sys;input=sys.stdin.readline

def get_time(s):
    t = 0
    last = ''
    for i in s:
        t += 1 + (i != last)
        last = i
    return t

from string import ascii_lowercase
def solve():
    s = input().strip()
    mans = float("-inf")
    ans = ""
    for l in ascii_lowercase:
        for pos in range(0,len(s)+1):
            st = list(s)
            st.insert(pos, l)
            a = get_time(st)
            if a > mans: 
                ans = "".join(st)
                mans = a
    print(ans)



    

    

for _ in range(int(input())): solve()

