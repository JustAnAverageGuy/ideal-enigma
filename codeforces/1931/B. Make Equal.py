import sys;input=sys.stdin.readline

def solve():
    n = int(input())
    A = list(map(int,input().strip().split()))
    s = sum(A)
    cont = s // n
    extra = 0
    for i in range(n):
        if A[i] < cont:
            if extra + A[i] < cont: print("NO"); return
            else:
                extra -= cont - A[i]
        else:
            extra += A[i] - cont
    print("YES")


for _ in range(int(input())): solve()

