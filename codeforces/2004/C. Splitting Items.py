import sys;input=sys.stdin.readline

def solve():
    n, k = map(int,input().strip().split())
    A = list(map(int,input().strip().split()))
    A.sort(reverse=True)
    for i in range(1,n,2):
        # i-1 to A, i to B
        x = min(A[i-1]-A[i], k)
        A[i] += x
        k -= x
    s = 0
    for i in range(n):
        s += A[i] * (1 - 2*(i%2))
    print(s)
    

for _ in range(int(input())): solve()

