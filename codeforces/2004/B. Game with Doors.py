import sys;input=sys.stdin.readline

def solve():
    l,r = map(int,input().strip().split())
    L,R = map(int,input().strip().split())
    a,b = sorted([(l,r),(L,R)])
    l,r = a
    L,R = b
    if r < L:
        print(1)
        return
    if r < R:
        print(r-L+2-(l == L))
        return
    else:
        print(R-L+2 - (l == L) - (r == R))

    

for _ in range(int(input())): solve()

