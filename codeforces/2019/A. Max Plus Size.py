import sys;input=sys.stdin.readline

def solve():
    n = int(input())
    A = list(map(int,input().strip().split()))
    if n == 1:
        print(A[0] + 1)
        return

    if n%2 == 0:
        print(max(A) + n//2)
    else:
        m = max(A[i] for i in range(0, n, 2))
        k = max(A[i] for i in range(1, n, 2))
        print(max(m + (n//2) + 1, k+(n//2) ))



for _ in range(int(input())): solve()

