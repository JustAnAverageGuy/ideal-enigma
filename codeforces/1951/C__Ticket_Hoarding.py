import sys;input=sys.stdin.readline

def solve():
    n,m,k = map(int,input().strip().split())
    A = list(map(int,input().strip().split()))
    A.sort()
    res = 0
    tickets = []
    sum_t = 0
    for i in range(n):
        if k == 0: break
        t = min(m, k)
        k -= t
        res += (A[i] + sum_t) * t
        tickets.append(t)
        sum_t += t
    print(res)

for _ in range(int(input())): solve()

