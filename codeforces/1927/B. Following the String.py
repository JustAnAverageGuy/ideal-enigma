import sys;input=sys.stdin.readline

from collections import defaultdict

def solve():
    n = int(input())
    A = list(map(int,input().strip().split()))
    letters = "qwertyuiopasdfghjklzxcvbnm"
    hmm = defaultdict(int)
    ans = []
    for i in A:
        ans.append(letters[hmm[i]])
        hmm[i] += 1
    print("".join(ans))

for _ in range(int(input())): solve()

