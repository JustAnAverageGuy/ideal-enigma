import sys;input=sys.stdin.readline

def gen(n):
    for i in range(1,n+1): yield i

def solve():
    n,k = map(int,input().strip().split())
    A = [None]*n
    
    t = gen(n)
    left = n%k
    
    lm = n // k
    for i in range(left):
        if i%2 == 0:
            for j in range(i,n,k): A[j] = next(t)
        else:
            for j in range(lm,-1,-1): A[j*k+i] = next(t)
    for i in range(left, k):
        if i%2 == 0:
            for j in range(i,n,k): A[j] = next(t)
        else:
            for j in range(lm-1,-1,-1): A[j*k+i] = next(t)


    print(*A)
    
    

for _ in range(int(input())): solve()

