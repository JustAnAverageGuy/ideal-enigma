import sys;input=sys.stdin.readline

def solve():
    n = int(input())
    r = 0
    l = n-1
    s = input().strip()
    for i in range(n):
        if s[i] == 'B': l = i; break
    for i in range(n):
        if s[i] == 'B': r = i
    print(max(0, r-l+1))

for _ in range(int(input())): solve()

