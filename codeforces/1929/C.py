import sys;input=sys.stdin.readline

def solve():
    k,x,a = map(int,input().strip().split())
    if a <= x: return False
    curr = a
    for _ in range(x):
        minb  = (a - curr)//(k-1) + 1
        curr -= minb
        if curr <= 0: return False
    return  curr * k > a

    
    
    
    

for _ in range(int(input())):print("YES" if solve() else "NO")

