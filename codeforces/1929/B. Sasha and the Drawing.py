import sys;input=sys.stdin.readline

def solve():
    n,k = map(int,input().strip().split())
    if k <= 2*n:
        print((k+1)//2)
        return
    if k <= 4*n:
        ans = n
        k -= 2*n
        if k <= 2*(n-2):
            ans += (k+1)//2
            k -= 2*((k+1)//2)
        else:
            if k == 2*n - 3:
                ans += (k+1)//2 
            else:
                ans += (k+1)//2 + 1
        print(ans)
    

for _ in range(int(input())): solve()

