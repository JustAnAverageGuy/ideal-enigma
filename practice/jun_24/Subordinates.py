n = int(input())
P = list(map(int, input().strip().split()))

from collections import defaultdict

children = defaultdict(list)
for i in range(n - 1):
    children[P[i]].append(i + 2)

subtree_size = [0] * (n + 1)

par = []
curr = [1]

while curr:
    node = curr.pop()
    if par and par[-1] == node:
        for c in children[node]:
            subtree_size[node] += 1 + subtree_size[c]
        par.pop()
        continue

    curr.append(node)

    for c in children[node]:
        curr.append(c)

    par.append(node)

print(*subtree_size[1:])

