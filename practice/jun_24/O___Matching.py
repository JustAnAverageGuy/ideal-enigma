import sys;input=sys.stdin.readline

n = int(input())
A = [list(map(int,input().strip().split())) for i in range(n)]

MOD = int(1e9+7)


from functools import cache

@cache
def cnt_perm(taken=0):
    currman = taken.bit_count()
    # print(f'{currman = }, {taken = :0b}', file=sys.stderr)
    if currman >= n: return 1
    s = 0
    for i in range(n):
        if not ((taken >> i)&1) and A[currman][i]:
            s += cnt_perm(taken | (1 << i))
            s %= MOD
    return s 

print(cnt_perm())

            

