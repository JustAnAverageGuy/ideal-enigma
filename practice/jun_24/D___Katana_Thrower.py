import sys;input=sys.stdin.readline
n,h = map(int,input().strip().split())
A = []
for i in range(n):
    a,b = map(int,input().strip().split())
    A.append((a,2))
    A.append((b,1))

A.sort(reverse=True)

s = 0
for a,t in A:
    if t == 1:
        s += 1
        h -= a
    else:
        k = (h + a-1)//a
        s +=  k
        h -= a*k

    if h <= 0:
        print(s)
        exit()



