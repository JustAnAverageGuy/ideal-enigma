def query(x , ans=False):
    print(f'{"-!"[ans]} {x}', flush=True)
    if not ans:
        k = int(input())
        return k
    return 0


def solve():
    cnt = int(input())
    nx = query(1)

    n = 1

    if nx == 0: query(1, True); return
    
    trailcnts = []

    while True:
        trailling_zeroes = nx - cnt - 1
        trailcnts.append(trailling_zeroes)
        cnt = nx
        nx = query((1<<trailling_zeroes))




for _ in range(int(input())): solve()

