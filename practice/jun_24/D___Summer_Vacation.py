import sys;input=sys.stdin.readline
n,m = map(int,input().strip().split())
A = [tuple( map(int,input().strip().split())) for _ in range(n)]

# try sorting by highest reward first

s = 0
d = 0

A.sort(key=lambda x: (-x[1], x[0]))
# A.sort()

print(A, file=sys.stderr)

for a, b in A:
    if d + a > m: continue
    s += b
    d += 1

print(s)



