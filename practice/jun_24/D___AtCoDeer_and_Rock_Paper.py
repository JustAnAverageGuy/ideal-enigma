s = input()

scr = 0
rockcknt = 0
papercnt = 0

for i in s:
    if i == 'g':
        if papercnt < rockcknt:
            scr += 1
            papercnt += 1
        else:
            rockcknt += 1
    else:
        if papercnt < rockcknt:
            papercnt += 1
        else:
            scr -= 1
            rockcknt += 1

print(scr)

