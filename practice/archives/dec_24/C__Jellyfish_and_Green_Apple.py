from math import gcd
import sys;input=sys.stdin.readline

def solve():
    n,m = map(int,input().strip().split())
    g = gcd(n,m)

    if (m//g) & (m//g-1) != 0: print(-1); return

    c = n % m
    while n:
        n = (2*n)%m
        c += n
    print(c)
    

for _ in range(int(input())): solve()

