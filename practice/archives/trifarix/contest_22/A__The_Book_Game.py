

n,k = map(int,input().strip().split())
A = list(map(int,input().strip().split()))
s = 0
MOD = int(1e9+7)

overflowed = False

for i in A:
    d = i%10
    if d == 0: continue
    if not overflowed:
        if (d == 1) or s in [0,1]: s += d
        else: s *= d
            
        if s >= MOD:
            overflowed = True
            s %= MOD
    else:
        if d == 1: s+= d
        else: s *= d
        s %= MOD
print(s)

    


