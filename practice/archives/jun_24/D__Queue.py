import sys;input=sys.stdin.readline

def solve():
    n = int(input())
    A = list(map(int,input().strip().split()))
    A.sort()
    s = 0
    c = 0
    for i in A:
        if s <= i: c += 1; s += i
    print(c)
    

solve()

