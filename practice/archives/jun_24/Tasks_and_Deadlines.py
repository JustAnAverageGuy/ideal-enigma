import sys;input=sys.stdin.readline

n = int(input())
A = []
for i in range(n):
    a,b = map(int,input().strip().split())
    A.append((a,b))
A.sort()

s = 0
rew = 0
for i,j in A:
    s += i
    rew += j - s

print(rew)

