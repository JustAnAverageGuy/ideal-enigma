s = input()
div = []

for i in range(0, 1000,8): div.append(str(i))

for t in div[::-1]:
    last = -1
    for c in t:
        last = s.find(c, last+1)
        if last == -1: break
    else:
        print("YES")
        print(t)
        exit()
print("NO")



