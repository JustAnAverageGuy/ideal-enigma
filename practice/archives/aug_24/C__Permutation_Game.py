n = int(input())
A = [0,*(map(int,input().strip().split()))]
rev = [0]*(n+1)
for i,j in enumerate(A): rev[j] = i
iswin = [False]*(n+1)

for i in range(n,0,-1):
    for j in range(rev[i]%i, n+1, i):
        if j in [0, rev[i]]: continue
        if A[j] > i and not iswin[j]: iswin[rev[i]] = True; break
    
print(*map(lambda x:"BA"[x], iswin[1:]), sep="")



