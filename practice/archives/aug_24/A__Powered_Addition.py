import sys;input=sys.stdin.readline

def solve():
    n = int(input())
    A = list(map(int,input().strip().split()))
    inc = [A[0]]
    for i in range(1,n): inc.append(max(inc[i-1], A[i]))
    diff = []
    for i,j in zip(A, inc): diff.append(j-i)
    print(max(i.bit_length() for i in diff))


for _ in range(int(input())): solve()

