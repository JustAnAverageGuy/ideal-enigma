import sys;input=sys.stdin.readline

def solve():
    n = int(input())
    A = list(map(int,input().strip().split()))
    A.append(0)
    me = ( 0,0 )
    fr = ( 0,0 )
    for i in range(n-1,-1,-1):
        mera = min(fr[-1], fr[-2])
        uska = A[i] +  min(me[-1], A[i+1]+ me[-2])
        me = ( me[-1], mera )
        fr = ( fr[-1], uska )

    print(fr[-1])



for _ in range(int(input())): solve()

