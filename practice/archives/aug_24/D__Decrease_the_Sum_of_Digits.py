import sys;input=sys.stdin.readline

def dig_sum(n):
    s = 0
    while n:
        s += n%10
        n //= 10
    return s

def solve():
    n,s = map(int,input().strip().split())
    curr = n
    while dig_sum(curr) > s:
        for k in range(19):
            if curr % (10**k) != 0:
                curr += (-curr) % (10**k)
                break
    print(curr - n)
            

    

for _ in range(int(input())): solve()

