import sys;input=sys.stdin.readline

def solve():
    n = int(input())
    s = input().strip()
    k = 0
    idx = [[], []]
    gos = []
    for i in s:
        a = idx[int(i)]
        if a:
            t = a.pop()
            gos.append(t)
            idx[1-int(i)].append(t)
        else:
            k += 1
            gos.append(k)
            idx[1-int(i)].append(k)
    print(k)
    print(*gos)


for _ in range(int(input())): solve()

