import sys;input=sys.stdin.readline

def solve():
    n = int(input())
    A = list(map(int,input().strip().split()))

    if n == 2: return A[0] == A[1]
    if n == 3: return (A[0] == A[1] == A[2]) or (A[0] ^ A[1] ^A[2] == 0)


    x = 0
    for i in A: x^= i
    if x == 0: return True
    
    cnt = 0
    cur = 0 
    
    for i in A:
        cur ^= i
        if cur == x:
            cnt += 1
            cur = 0
    return cnt >= 3

    

for _ in range(int(input())): print("YES" if solve() else "NO")

