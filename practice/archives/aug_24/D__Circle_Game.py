import sys;input=sys.stdin.readline

from math import isqrt
def solve():
    d,k = map(int,input().strip().split())
    a = isqrt((d*d)//(2*k*k))
    # a is the largest a such that (a*k, a*k) is in the circle
    # if ((a+1)*k, a*k) is also in the circle then p1 wins, else p2 wins
    return ((a+1)*(a+1) + a*a)*k*k <= d*d
    


    

for _ in range(int(input())): print("Ashish" if solve() else "Utkarsh")

