import sys;input=sys.stdin.readline
from collections import defaultdict

def nc3(n):
    if n <= 2: return 0
    return n*(n-1)*(n-2)//6
def nc2(n):
    if n <= 1: return 0
    return n*(n-1)//2

def solve():
    n = int(input())
    A = list(map(int,input().strip().split()))
    c = defaultdict(int)
    for i in A: c[str(i)] += 1
    b = list(c.items())
    b.sort(key=lambda x: int(x[0]))
    cn = [x[1] for x in b]
    s = sum(map(nc3, cn))
    ss = 0
    for i in cn:
        s += ss * nc2(i)
        ss += i
    print(s)


for _ in range(int(input())): solve()

