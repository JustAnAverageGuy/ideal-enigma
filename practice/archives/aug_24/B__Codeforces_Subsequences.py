from math import prod
k = int(input())

l = 0
r = 40

while r-l > 1:
    m = (l+r)//2
    if pow(m, 10) >= k:
        r = m
    else:
        l = m

freq = [l]*10

 
for i in range(10):
    freq[i] += 1
    if prod(freq) >= k: break

s = []
for a,b in zip("codeforces", freq[::-1]): s.append(a*b)
print(*s, sep="")


