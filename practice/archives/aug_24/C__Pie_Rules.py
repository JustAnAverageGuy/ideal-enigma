n = int(input())
A = list(map(int,input().strip().split()))
opt = [0]*(n+1)
suf = [0]*(n+1)
for i in range(n-1,-1,-1):
    suf[i] += suf[i+1] + A[i]

for i in range(n-1, -1, -1):
    opt[i] = max(opt[i+1], suf[i+1]-opt[i+1])

print(min(suf[0]-opt[0],opt[0]), max(suf[0]-opt[0],opt[0]))

