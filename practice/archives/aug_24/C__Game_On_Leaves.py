import sys;input=sys.stdin.readline

def solve():
    n,x = map(int,input().strip().split())
    c = 0
    for _ in range(n-1):
        a,b = map(int,input().strip().split())
        if x in (a,b): c += 1
    if c <= 1: return 1
    return (n+1)%2

    

for _ in range(int(input())): print("Ayush" if solve() else "Ashish")

