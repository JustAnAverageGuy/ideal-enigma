n,m = map(int,input().strip().split())

players = []
for i in range(m):
    l,r,t,c = map(int,input().strip().split())
    players.append((i,l,r,t,c))

profit = 0

for section in range(1,n+1):
    runners = [(i[3],i[0],i[4]) for i in players if i[2]>=section>=i[1]]
    if runners:
        winner = min(runners)
        profit += winner[-1]
print(profit)




















