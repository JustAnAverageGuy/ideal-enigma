import sys;input=sys.stdin.readline

def solve():
    n,d,h = map(int,input().strip().split())
    Y = list(map(int,input().strip().split()))
    Y.sort()
    A = (d*h)/2
    c = n
    for i in range(n-1): c -= (1 - min(Y[i+1] - Y[i], h)/h)**2
    print(A*c)
        
    

for _ in range(int(input())): solve()