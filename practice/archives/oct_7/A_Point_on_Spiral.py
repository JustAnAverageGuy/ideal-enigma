x,y = map(int,input().strip().split())

k = -x

if -k<=y<=k-1:
    print(4*k-1)
    exit(0)

k = -y

if 1-k <= x <= 1+k:
    print(4*k)
    exit(0)

k = x-1

if 1-k <= y <= 1+k:
    print(4*k+1)
    exit(0)

k = y-1

if -(1+k) <= x <= k:
    print(4*k+2)
    exit(0)

