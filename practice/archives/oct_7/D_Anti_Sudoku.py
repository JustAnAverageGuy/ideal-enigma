import sys;input=sys.stdin.readline


def solve():
    A = [[int(i) for i in input().strip()] for i in range(9)]
    for x in range(3):
        for y in range(3):
            X = 3*x + y
            Y = 3*y + x
            A[Y][X] = A[Y][X]%9 + 1
    for i in A:
        print(*i,sep='')
for _ in range(int(input())): solve()