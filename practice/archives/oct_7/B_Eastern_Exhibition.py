# ⠜⠋⢠⣷⢻⣿⣿⣶⣾⣿⣿⣿⣿⠿⣛⣥⣾⣿⠿⠟⠛⠉⠄⠄

import sys;input=sys.stdin.readline

def solve():
    n = int(input())
    A = [tuple(map(int,input().strip().split())) for i in range(n)]
    mi = float('inf')
    cnt = 0
    for i in A:
        d = 0
        for j in A: d += abs(j[0] - i[0]) + abs(j[1] - i[1])
        if d < mi:
            cnt = 1
            mi = d
        elif d == mi:
            cnt += 1
        
    print(cnt)
    

for _ in range(int(input())): solve()