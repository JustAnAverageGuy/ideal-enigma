import sys;input=sys.stdin.readline

def solve():
    n,k = map(int,input().strip().split())
    a = n
    for i in range(k-1):
        digits = [int(j) for j in str(a)]
        a0 = a
        a += max(digits) * min(digits)
        if a0 == a:
            break
    print(a)
        
    

for _ in range(int(input())): solve()