# ⣆⢕⠄⢱⣄⠛⢿⣿⣿⣿⣿⣿⣿⣿⣿⣿⣿⣿⣿⣿⣿⣿⣿⣿⠿⢁⢕⢕⠕⢁
# ⣿⣦⡀⣿⣿⣷⣶⣬⣍⣛⣛⣛⡛⠿⠿⠿⠛⠛⢛⣛⣉⣭⣤⣂⢜⠕⢑⣡⣴⣿


ax,ay,bx,by,cx,cy = map(int,input().strip().split())

def dist(a,b): return (a[0]-b[0])*(a[0]-b[0]) + (a[1]-b[1])*(a[1]-b[1])

u = (bx-ax,by-ay)
v = (cx-ax,cy-ay)

print("Yes" if (u[1]*v[0]-u[0]*v[1]) and dist(u,(0,0)) == dist(u,v) else "No")