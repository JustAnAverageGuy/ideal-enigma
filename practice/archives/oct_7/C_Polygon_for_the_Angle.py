from math import gcd


for _ in range(int(input())):
    theta = int(input())
    g = gcd(theta, 180)
    n_min = 180//g
    assert (n_min * theta)%180 == 0
    k = (n_min * theta)//180
    print(n_min + (n_min)*(k == n_min - 1))