import sys;input=sys.stdin.readline

n,sx,sy = map(int,input().strip().split())

up=down=left=right=0

for i in range(n):
    x,y = map(int,input().strip().split())
    right += (x>sx)
    left  += (x<sx)
    up    += (y>sy)
    down  += (y<sy)

m,i = max((right,0), (left,1), (up,2), (down,3))


print(m)

print(sx+(i==0)-(i==1),sy+(i==2)-(i==3))