from math import gcd

def points_to_line(x1,y1,x2,y2):
    a = y1 - y2
    b = x2 - x1
    c = -a*x1 - b*y1
    p = (a,b,c)
    g = gcd(gcd(a,b),c)
    
    p = (i//g for i in p)
    
    if a < 0 or (a==0 and  b < 0): p = (-i for i in p)
    return tuple(p)

n,x0,y0 = map(int,input().strip().split())

eliminated = set()

cnt = 0
for _ in range(n):
    x,y = map(int,input().strip().split())
    p = points_to_line(x0,y0,x,y)
    if p not in eliminated:
        cnt += 1
        eliminated.add(p)
print(cnt)