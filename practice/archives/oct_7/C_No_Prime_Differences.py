import sys;input=sys.stdin.readline

    

for _ in range(int(input())):
    n,m = map(int,input().strip().split())
    if m%2 == 0:
        for i in range(1,n*m,m): print(*range(i, i+m))
    elif n%2==0:
        for i in range(1, n+1): print(*range(i,i + (m-1)*n + 1,n))
    else:
        for i in range(1,n+1): print(*range(1 + m * ((i-1)//2 + (n//2)*(i%2)),1 + m * ((i-1)//2 + (n//2)*(i%2)) + m))