import sys;input=sys.stdin.readline




def solve():
    n = int(input())
    A = list(map(int,input().strip().split()))
    if n <= 2: return 0
    max_changes = n-2
    for i in range(n):
        for j in range(i+1, n):
            changes = 0
            d = (A[j] - A[i]) / (j - i)
            for k in range(n):
                if abs(A[k] - A[i] - (k-i)*d) >= 1e-3 :
                    changes += 1
            max_changes = min(max_changes, changes)
    return max_changes
    
    

for _ in range(int(input())): print(solve())