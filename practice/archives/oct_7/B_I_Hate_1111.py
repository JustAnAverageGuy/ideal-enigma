from collections import deque


def gen_bfs(limit=300):
    A = set()
    hmm = [11,111,1_111,11_111,111_111,1_111_111,11_111_111,111_111_111]
    qu = deque([11])
    while qu:
        a = qu.popleft()
        A.add(a)
        print(a)
        for i in hmm:
            if i + a <= limit:
                qu.append(i+a)
    return A