n,x = map(int,input().strip().split())
A = list(map(int,input().strip().split()))
from collections import defaultdict
seen = defaultdict(int)
seen["0"] += 1

s = 0
ans = 0
for i in A:
    s += i
    ans += seen[str(s - x)]
    seen[str(s)] += 1
print(ans)


