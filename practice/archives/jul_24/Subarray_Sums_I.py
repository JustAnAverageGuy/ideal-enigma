n,x = map(int,input().strip().split())
A = list(map(int,input().strip().split()))

cnt = 0

l = 0
r = 0

curs = 0

n = len(A)
while r < n:
    curs += A[r]
    while curs >= x:
        if curs == x: cnt += 1
        curs -= A[l]
        l += 1
    r += 1
print(cnt)
            



