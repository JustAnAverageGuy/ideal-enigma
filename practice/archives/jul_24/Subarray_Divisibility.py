from collections import defaultdict


n = int(input())
A = list(map(int,input().strip().split()))

pref = defaultdict(int)
pref["0"] = 1

s = 0
ans = 0
for i in A:
    s += i
    s %= n
    ans += pref[str(s)]
    pref[str(s)] += 1
print(ans)



