import sys;input=sys.stdin.readline

def solve():
    ax,ay, bx,by , cx,cy = map(int,input().strip().split())
    p12 = (bx-ax, by-ay)
    p13 = (cx-ax, cy-ay)
    cros = p13[1] * p12[0] - p13[0]*p12[1]
    if cros > 0:
        print("LEFT")
    elif cros < 0:
        print("RIGHT")
    else:
        print("TOUCH")
    

for _ in range(int(input())): solve()

