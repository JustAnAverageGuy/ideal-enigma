import sys;input=sys.stdin.readline
h,w = map(int,input().strip().split())
A = [input().strip() for i in range(h)]


cnt = 0
for i in A:
    fnccnt = 0
    for c in i:
        if c == '*': fnccnt += 1; continue
        if c == '@' and fnccnt == 1: cnt += 1
print(cnt)



