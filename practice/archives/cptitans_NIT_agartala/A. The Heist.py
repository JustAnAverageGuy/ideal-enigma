import sys;input=sys.stdin.readline


def solve():
    x1,y1,r1,x2,y2,r2 = map(int,input().strip().split())
    dc = (x1-x2)**2 + (y1-y2)**2
    return dc <= (r1+r2)**2
    
    

for _ in range(int(input())): print("YES" if solve() else "NO")


