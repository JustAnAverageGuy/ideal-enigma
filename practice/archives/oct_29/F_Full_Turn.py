from collections import defaultdict
from math import gcd

import sys;input=sys.stdin.readline

def solve():
    n = int(input())
    d = defaultdict(int)    
    for i in range(n):
        x,y,u,v = map(int,input().strip().split())
        dx,dy = u-x, v-y
        g = gcd(dx,dy)
        d[(dx//g , dy//g)] += 1
    ans = 0
    for i in d: ans += d[i] * d.get((-i[0],-i[1]),0)
    print(ans//2)
    
