a,b = map(int,input().strip().split())
d = (a-b)
if d <  0: print(0); exit()
if d == 0: print('infinity'); exit()

f = []
i = 1
while i*i <= d:
    if d%i == 0:
        f.append(i)
        if d != i*i: f.append(d//i)
    i += 1

f.sort()
from bisect import bisect_right
print(len(f) - bisect_right(f, b) )




















