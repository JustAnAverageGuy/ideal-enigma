r = tuple(map(int,input().strip().split()))

c = 0
while all(r):
    a,b = r
    if a > b:
        d,a = divmod(a,b)
        c += d
    else:
        d,b = divmod(b,a)
        c += d
    r = (a,b)

print(c)
    



















