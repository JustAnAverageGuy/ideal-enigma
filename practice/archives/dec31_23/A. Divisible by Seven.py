s = input()
from collections import Counter
x = Counter(s)
for i in '1689': x[i] -= 1

aax = [i*x[i] for i in x if i != 0]
aa = "".join(aax)

from itertools import cycle

pos_of_10 = cycle([1,3,2,6,4,5])
rem = 0
for i,k in zip(aa[::-1], pos_of_10):
    rem += (int(i)*k)
    rem %= 7
target = (-rem)%7

from itertools import permutations
p = {int("".join(i))%7 : "".join(i) for i in permutations("1869",4)}
print(aa,end='')
print(p[target]+'0'*x[0])



















