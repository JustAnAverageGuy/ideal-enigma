n = int(input())
A = list(map(int,input().strip().split()))

ks = []
i = 1
while i*i <= n:
    if n%i == 0:
        if i    >= 3: ks.append(i)
        if n//i >= 3: ks.append(n//i)
    i += 1

def check(k): 
    return any(all(A[i::n//k]) for i in range(n//k))

print("YES" if any(check(k) for k in ks) else "NO")


