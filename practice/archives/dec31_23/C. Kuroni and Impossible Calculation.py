n,m = map(int,input().strip().split())

A = list(map(int,input().strip().split()))

if m == 1 or n > m: print(0); exit()

p = 1
for i in range(n):
    for j in range(i+1,n):
        p *= abs(A[i] - A[j])
        p %= m
        if p == 0: print(0); exit()
print(p)


