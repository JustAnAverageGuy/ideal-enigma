n = int(input())
A = list(map(int,input().strip().split()))

longest = 0
prev = A[0]
curr = 0
for i in range(1,n):
    if A[i]<=prev:
        curr += 1
        longest = max(curr, longest)
    else:
        curr = 0
    prev = A[i]
print(longest)


