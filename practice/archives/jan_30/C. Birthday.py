n = int(input())
A = list(map(int,input().strip().split()))
from collections import deque

hmm = deque([])
f = 0
for i in sorted(A):
    if f: hmm.append(i)
    else: hmm.appendleft(i)
    f = 1-f
print(*hmm)




















