a,b,c = map(int,input().strip().split())

cnt = 0
if a == b == c:
    print(0 if c%2 else -1)
    exit()

while not any(i&1 for i in [a,b,c]):
    if a == b == c:
        print(cnt if c%2 else -1)
        exit()
    a,b,c = (b+c)//2, (a+c)//2, (a+b)//2
    cnt += 1
print(cnt)

