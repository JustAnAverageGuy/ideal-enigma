import sys


s = input()
n = int(input())
hmm = -1
for i,j in enumerate(s,1):
    if j != '1': hmm = i; break
else:
    print(1)
    exit()
# print(hmm,file=sys.stderr)
if n < hmm: print(1); exit()
print(s[hmm-1])


