from collections import defaultdict
import sys;input=sys.stdin.readline

def solve():
    n,x = map(int,input().strip().split())
    A = list(map(int,input().strip().split()))
    hmm = defaultdict(int)
    for i in A: hmm[i] += 1
    cnt = 0
    for k in sorted(hmm):
        if hmm[k] == 0: continue
        m = min(hmm[k*x], hmm[k])
        hmm[k*x] -= m
        hmm[k] -= m
        cnt += hmm[k]
    print(cnt)

    

for _ in range(int(input())): solve()

