def hash(s:str):
    p = 27
    i = 1
    sm = 0
    for c in s:
        sm += i*(ord(c) - ord('a') + 1)
        i *= p
    return sm
s = input()
hss = hash(s)

t = input()
hst = hash(t)

if hst == hss: print("Yes"); exit()
n = len(t)

pw = pow(27,n-1)
for rot in range(1,n):
    hst = (hst//27) + (hst%27)*pw
    if hss == hst: print("Yes"); exit()
