from collections import defaultdict

cnt = defaultdict(int)

for _ in range(int(input())):
    s = input()
    cnt[s] += 1
for _ in range(int(input())):
    s = input()
    cnt[s] = max(cnt[s] - 1, 0)
print(max(cnt.values()))

