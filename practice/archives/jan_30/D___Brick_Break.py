n = int(input())
A = list(map(int,input().strip().split()))
ans = 0

i = 1
p = 0
while p < n:
    while p<n and A[p] != i:
        p += 1
        ans += 1
    if p<n: i += 1; p += 1
if i == 1: print(-1)
else: print(ans)


