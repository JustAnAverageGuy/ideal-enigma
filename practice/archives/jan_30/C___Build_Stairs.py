n = int(input())
A = list(map(int,input().strip().split()))
    
for i in range(n-1,0,-1):
    if A[i-1] - A[i] > 1: print("No"); exit()
    else:
        if A[i-1] - A[i] == 1: A[i-1] -= 1
print("Yes")





