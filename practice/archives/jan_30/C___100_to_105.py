n = int(input())
if n > 100*101+100+101: print(1); exit()
if n < 100: print(0); exit()

from functools import cache 

@cache 
def f(n):
    if n < 100: return False
    if 100 <= n <= 105: return True
    return f(n-105) or f(n-104) or f(n-103) or f(n-102) or f(n-101) or f(n-100)
print(int(f(n)))

