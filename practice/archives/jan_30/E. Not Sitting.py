import sys;input=sys.stdin.readline

def solve():
    n,m = map(int,input().strip().split())
    A = []
    corners = [(0,0),(n-1,0),(0,m-1),(n-1,m-1)]
    for r in range(n):
        for c in range(m):
            A.append(max(abs(r-y)+abs(c-x)  for (y,x) in corners))
    print(*sorted(A))
    

for _ in range(int(input())): solve()

