look = {}
for i in range(3):
    A = list(map(int,input().strip().split()))
    for j in range(3):
        look[A[j]] = 3*i+j

seen = [0]*9
for _ in range(int(input())):
    n = int(input())
    if n in look: seen[look[n]] = 1

for i in range(3):
    if all(seen[i::3]) or all(seen[3*i:3*i+3]): print("Yes"); exit()
if seen[4] and ((seen[0] and seen[8]) or (seen[2] and seen[6])): print("Yes"); exit()
print("No")
    



