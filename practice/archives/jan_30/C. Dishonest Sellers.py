n,k = map(int,input().strip().split())
A = list(map(int,input().strip().split()))
B = list(map(int,input().strip().split()))

hmm = sorted(zip(A,B), key=lambda x: x[0] - x[1])

x = k
while x < n and (hmm[x][0] < hmm[x][1]): x += 1

print(
    sum(i[0] for i in hmm[:x]) + sum(i[1] for i in hmm[x:])
)




















