n,K = map(int,input().strip().split())
A = list(map(int,input().strip().split()))
ans = [0, abs(A[0] - A[1])]

for i in range(2,n):
    ans.append(
        min(ans[-k] + abs(A[i] - A[i-k]) for k in range(1, K+1) if i-k>=0 )
    )
print(ans[-1])


