s = input()
t = input()

n = len(s)
fstdif = -1

for i in range(n):
    if s[i] < t[i]: 
        fstdif = i
        break
if ord(t[fstdif]) - ord(s[fstdif]) > 1: print(s[:fstdif] + chr(ord(s[fstdif]) + 1) + s[fstdif+1:]); exit()

def get_next(s):
    if s[-1] != 'z': return s[:-1] + chr(ord(s[-1]) + 1)
    return get_next(s[:-1]) + 'a'

nex = get_next(s)

if nex == t: 
    print("No such string")
else:
    print(nex)


