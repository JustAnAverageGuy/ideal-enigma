n = int(input())
A = list(map(int,input().strip().split()))

MOD = 998244353

a = n // 3 
b = n // 6

num = 1
for i in range(b+1, a+1):
    num *= i
    num %= MOD
den = 1
for i in range(1, b+1):
    den *= i
    den %= MOD

num *= pow(den, -1, MOD)
num %= MOD

for i in range(0,n,3):
    a,b,c = A[i:i+3]
    if a == b == c:
        num *= 3
        num %= MOD
        continue
    a,b,c = sorted(A[i:i+3])
    if a == b:
        num *= 2
        num %= MOD
print(num)



