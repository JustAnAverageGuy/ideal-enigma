a,b = map(int,input().strip().split());

if a == 0: print(3*b); exit()
if b == 0: print(2*a); exit()


l = 1
r = 6*(a+b)

def check(m):
    only2 = m//2 - m // 6
    only3 = m//3 - m // 6
    both = m//6
    hmma = max(a - only2, 0)
    hmmb = max(b - only3, 0)
    return hmma + hmmb <= both

while r-l > 1:
    m = l + (r-l)//2
    if check(m): r = m
    else: l = m

print(r)


