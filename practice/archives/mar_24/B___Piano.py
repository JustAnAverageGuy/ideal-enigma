w,b = map(int,input().strip().split())
s = "wbwbwwbwbwbw"*20

prefw = [0]
prefb = [0]

for i in s:
    prefw.append(prefw[-1]+(i=='w'))
    prefb.append(prefb[-1]+(i=='b'))


for l in range(1,len(prefw)):
    for r in range(l, len(prefw)):
        if (prefw[r] - prefw[l-1] == w) and prefb[r] - prefb[l-1] == b:
            print("Yes")
            exit()
print("No")


