carrot = 2
calories = 0
choc = 100
pack = 1
egg = 1

A = list(map(int,input().strip().split()))
bowl = [calories]
dish = []
carrot = A[0]
i = 1
while carrot != 0:
    choc = A[i]; i+=1
    bowl.append(choc)
    bowl.append(bowl[-1] * pack)
    choc = bowl.pop()
    bowl.append(choc+bowl[-1])
    bowl.append(pack)
    bowl.append(bowl[-1]+egg)
    pack = bowl.pop()
    carrot -= 1
dish += bowl
print(*dish)


