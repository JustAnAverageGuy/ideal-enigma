A = list(map(int, input().strip().split()))
ops = [
    *map(lambda x: {
        "*": lambda a, b: a * b,
        "+": lambda a, b: a + b
    }[x],
         input().split())
]

from itertools import combinations

huh = []
for i in combinations(range(4), 2):
    remaining = [j for j in range(4) if j not in i]
    hmm = [ops[0](A[i[0]], A[i[1]]), A[remaining[0]], A[remaining[1]]]
    for x in combinations(range(3), 2):
        rem = [j for j in range(3) if j not in x]
        huh.append(ops[2](ops[1](hmm[x[0]], hmm[x[1]]), hmm[rem[0]]))
print(min(huh))

