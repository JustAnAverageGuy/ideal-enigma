s = input()
ref = s[::-1]

mirrpairs = {
    "AA","bd","HH","II","MM","OO","oo","pq","TT","UU","VV","vv","WW","ww","XX","xx","YY"
}
hmm = dict()
for i in mirrpairs:
    hmm[i[0]] = i[1]
    hmm[i[1]] = i[0]

print(["NIE","TAK"][all(i in hmm and hmm[i]==j for i,j in zip(s,ref))])

