# ⠀⣶⣿⣦⣜⣿⣿⣿⡟⠻⣿⣿⣿⣿⣿⣿⣿⡿⢿⡏⣴⣺⣦⣙⣿⣷⣄⠀⠀⠀
# ⠀⣯⡇⣻⣿⣿⣿⣿⣷⣾⣿⣬⣥⣭⣽⣿⣿⣧⣼⡇⣯⣇⣹⣿⣿⣿⣿⣧⠀⠀
# ⠀⠹⣿⣿⣿⣿⣿⣿⣿⣿⣿⣿⣿⣿⣿⣿⣿⣿⣿⣿⠸⣿⣿⣿⣿⣿⣿⣿⣷⠀

n = int(input())
if n <3: print(0); exit(0)
A = list(map(int,input().strip().split()))
pre = [0]
for i in range(n): pre.append(pre[-1] + A[i])

if pre[-1]%3: print(0);exit(0)

k = pre[-1]//3

ans = 0

leftends  = []
rightends = []
for i in range(1,n):
    if pre[i] == k:
        leftends.append(i)
    if pre[i] == 2*k:
        rightends.append(i)
        
# print(leftends,rightends)
for r in rightends:
    ans += bisect_right(leftends,r-1)
print(ans)