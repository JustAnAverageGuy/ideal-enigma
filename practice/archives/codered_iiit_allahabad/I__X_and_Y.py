
import sys;input=sys.stdin.readline

def solve():
    n,x,y = map(int,input().strip().split())
    A = list(map(int,input().strip().split()))

    for i,j in zip(A,A[1:]):
        if j < i: break
    else:
        return True

    if n == 2: return True
    x,y = sorted([x,y])
    if n == 3: return A[0] <= x or y <= A[2]
    

for _ in range(int(input())): print("YES" if solve() else "NO")
