import sys;input=sys.stdin.readline
MOD = 998244353
def solve():
    n = int(input())
    a = int(input().strip()[-3:].lstrip('0')) - 1 
    b = int(input().strip()[-3:].lstrip('0')) - 1 
    p = sum((a//i == b//i) for i in range(1,n+1))
    print((p * pow(n,-1,MOD))%MOD)

for _ in range(int(input())): solve()

