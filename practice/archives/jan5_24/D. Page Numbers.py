A = list(map(int,input().strip().split(',')))
A = sorted(set(A))
ranges = []
curr = [A[0]]
for j in A[1:]:
    if j != curr[-1]+1:
        if len(curr) > 1: ranges.append(f'{curr[0]}-{curr[1]}')
        else: ranges.append(str(curr[0]))
        curr = [j]
    else:
        if len(curr) == 1:
            curr.append(j)
        else:
            curr[1] = j

        

if len(curr) > 1: ranges.append(f'{curr[0]}-{curr[1]}')
else: ranges.append(str(curr[0]))

print(",".join(ranges))



















