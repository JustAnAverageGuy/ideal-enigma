n = int(input())
A = list(map(int,input().strip().split()))
presnet = [0]*101
cols = [0]*101
for i in A: presnet[i] = 1

for j in range(1,101):
    if cols[j] or not presnet[j]: continue
    for k in range(j,101,j):
        cols[k] = j

print( len(set(cols)) - 1)

















