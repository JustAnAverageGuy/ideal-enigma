n = int(input())
A = list(map(int,input().strip().split()))
A.sort()

sm = 0

for i in range(n//2):
    sm += A[i]*A[i] + A[n-i-1]*A[n-i-1] + 2*A[i]*A[n-i-1]

print(sm)




















