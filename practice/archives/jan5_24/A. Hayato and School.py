import sys;input=sys.stdin.readline

def solve():
    n = int(input())
    A = list(map(int,input().strip().split()))
    odds = []
    evens = []
    for i,x in enumerate(A,1):
        if x%2: odds.append(i)
        else: evens.append(i)
    if len(odds) == 0: print("NO"); return
    if len(odds) >= 3: print("YES"); print(*odds[:3]); return
    if len(evens) >= 2: print("YES"); print(odds[0], *evens[:2]); return
    print("NO")
    return 


for _ in range(int(input())): solve()




















