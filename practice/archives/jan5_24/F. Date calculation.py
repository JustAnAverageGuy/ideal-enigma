year = int(input())
n = int(input())

is_leap = year%400==0 or (year%4==0 and year%100 != 0)

months = [0,31,28+is_leap,31,30,31,30,31,31,30,31,30,31]

m = 12
s = sum(months)

while True:
    if n > s: break
    s -= months[m]
    m -= 1

print(n-s, m+1)


















