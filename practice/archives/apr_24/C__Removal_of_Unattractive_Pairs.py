import sys;input=sys.stdin.readline

from collections import Counter

def solve():
    n = int(input())
    s = Counter(input().strip())
    _,cnt = s.most_common(1)[0]
    if 2*cnt < n: print(n%2); return
    print(2*cnt-n)


for _ in range(int(input())): solve()

