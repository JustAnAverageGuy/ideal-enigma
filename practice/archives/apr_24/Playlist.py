from collections import defaultdict 

n = int(input())
A = list(map(int,input().strip().split()))

d = defaultdict(lambda : -1)

# last ocurrence of x = d[x]
l = 0
maxl = 1

for i,k in enumerate(A):
    

    l = max(l, d[k]+1)
    ln = i - l + 1
    maxl = max(maxl, ln)


    d[k] = i
print(maxl)

    


    

