import sys;input=sys.stdin.readline

def solve():
    n = int(input())
    A = list(map(int,input().strip().split()))
    seen = [False]*2
    for i in A[::-1]:
        if (i >= 1 and seen[0]) or (i > 1 and seen[1]): return False
        if i < 2: seen[i] = True
    return True

for _ in range(int(input())): print("YES" if solve() else "NO")

