n = int(input())
A = list(map(int,input().strip().split()))
cnts = [0]*101
for i in A: cnts[i] += 1

micst = float("inf")
ans = 1
for t in range(1,101):
    cst = 0
    for i in range(1, t-1): cst += cnts[i] * (t-1 - i)
    for i in range(t+1,101): cst += cnts[i] * (i-t-1)
    if micst > cst:
        micst = cst
        ans = t
print(ans, micst)
    

