n,x = map(int,input().strip().split())
A = list(map(int,input().strip().split()))
A.sort()

i = 0
j = n - 1
ans = 0
while i <= j:
    if A[j] + A[i] <= x:
        j -= 1
        i += 1
        ans += 1
        continue
    else:
        j -= 1
        ans += 1
        continue
print(ans)


