import sys;input=sys.stdin.readline

def solve():
    n = int(input())
    n -= 1
    strt = 1
    ledig = 1
    while True:
        if n <= ledig * (10*strt - 1):
            poore, n = divmod(n, ledig)
            # if n == 0:
            #     poore -= 1
            #     n = ledig
            strt += poore 
            # print(n, ledig, strt, file=sys.stderr)
            print(str(strt)[n])

            return
        else:
            # subtract off
            n -= ledig * (10*strt - 1)
            strt *= 10
            ledig += 1



for _ in range(int(input())): solve()

