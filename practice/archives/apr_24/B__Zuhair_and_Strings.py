n,k = map(int,input().strip().split())
s = input()
###

if n == 1: print(1); exit()


currc = s[0]
lastidx = 0

# from string import ascii_lowercase
# hmm_ranges = {i:[] for i in ascii_lowercase}

from collections import defaultdict
hmm_ranges = defaultdict(int)

for i in range(1, n):
    if s[i] != currc:
        ln = i - lastidx
        hmm_ranges[currc] += (ln // k)
        currc = s[i]
        lastidx = i
ln = n - lastidx
hmm_ranges[currc] += (ln // k)

print(max(hmm_ranges.values()))



