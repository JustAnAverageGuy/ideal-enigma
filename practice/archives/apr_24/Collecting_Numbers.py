n = int(input())
A = list(map(int,input().strip().split()))

# try to collect 1 -> 2 -> 3 -> ... 
# but for each number (i,i+1) such that i+1 appears before in the sequence
# this will not be possible and a new chain has to be started

posn = [0]*(n+1)
for i in range(n):
    posn[A[i]] = i

cnt = 1
for j in range(1,n):
    cnt += posn[j] > posn[j+1]
print(cnt)



