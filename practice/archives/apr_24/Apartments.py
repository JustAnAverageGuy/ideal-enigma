n,m,k = map(int,input().strip().split())
A = list(map(int,input().strip().split()))
B = list(map(int,input().strip().split()))
A.sort()
B.sort()
i = 0
j = 0
ans = 0
while i < n and j < m:
    if A[i] - k <= B[j] <= A[i] + k:
        ans += 1
        i += 1
        j += 1
        continue
    if A[i] -k > B[j]:
        j += 1
    else:
        i += 1
print(ans)

