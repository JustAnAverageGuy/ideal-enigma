n = int(input())
A = list(map(int,input().strip().split()))
ans = 1
MOD = int(1e9+7)

from collections import defaultdict

cnt = defaultdict(lambda : 1)


for i in A:
    cnt[i] += 1

for x in cnt.values():
    ans *= x
    ans %= MOD

print((ans - 1) % MOD)
    





