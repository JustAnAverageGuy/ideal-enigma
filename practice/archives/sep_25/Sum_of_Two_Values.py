import sys


n, target = map(int,input().strip().split())
A = list(map(int,input().strip().split()))

if n == 1:
    print("IMPOSSIBLE")
    sys.exit(0)

orig = [(j, i) for i,j in enumerate(A, 1)]

orig.sort()

l = 0
r = 1
while l < r:
    ll = orig[l][0]
    while r < n and ll + orig[r][0] < target:
        r += 1
    if ll + orig[r][0] == target:
        print(orig[l][1], orig[r][1])
        sys.exit(0)
    l += 1
print("IMPOSSIBLE")

