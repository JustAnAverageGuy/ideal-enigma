import sys;input=sys.stdin.readline

def f(x: int):
    ans = 0
    while x:
        ans <<= 1
        ans += x&1
        x >>= 1
    return ans

def solve():
    n = int(input())
    if n == 0: return True
    while n and n&1 == 0: n >>= 1
    s = f'{n:b}'
    return s == s[::-1] and (len(s)%2==0 or s[len(s)//2] == '0')

    

for _ in range(int(input())): print("YES" if solve() else "NO")

