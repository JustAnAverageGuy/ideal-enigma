def query(x: int)->bool:
    print(f"? {x}", flush=True)
    return input().strip() == "YES"

l = 0
r = int(1e9)

while r-l > 1:
    m = l + (r-l)//2
    if not query(m):
        r = m
    else:
        l = m
print(f'! {r}')

