import sys;input=sys.stdin.readline

def query(indices: list[int], typ=2) -> int:
    "typ = 2: normal query\ntyp = 1: submit query"
    if typ != 2:
        print('!',*indices, flush=True)
        return True
    if len(indices) <= 1: return 0
    print(f'? {len(indices)}',*indices, flush=True)
    return int(input())

def solve():
    n = int(input())
    query( [*range(1,n+1)]*2 ,1)
    

for _ in range(int(input())): solve()

