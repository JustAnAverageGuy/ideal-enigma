
from itertools import count
import sys;input=sys.stdin.readline

def solve(n:int, start = 1)->int:
    last = 0
    for k in count(0):
        # number of digits if all numbers upto k digits are included
        digs = k * (10**k) - (10**k-1)//9
        if digs > n: break
        last = k
    remaining = n - ((last-1) * (10**(last-1)) - (10**(last-1)-1)//9 if last != 0 else 0)
    return (last, remaining)
        
    

for _ in range(int(input())): 
    n = int(input())
    print(solve(n))

