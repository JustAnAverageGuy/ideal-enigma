n = int(input())

ans = [[*range(n)]]

print(*ans[0])


for row in range(1, n):
    mexes = [row]
    for col in range(1, n):
        meheap = set(range(2 * n))
        meheap -= set(mexes)
        for ro in range(row):
            meheap.discard(ans[ro][col])
        mexes.append(min(meheap))
    ans.append(mexes)
    print(*mexes)


