from functools import cmp_to_key
import sys


def cmp(i: int, j: int) -> int:
    print(f"? {i} {j}", flush=True)
    return -1 if input().strip() == "YES" else 1


n = int(input())


res = sorted([*range(1, n + 1)], key=cmp_to_key(cmp))

# print(res, flush=True, file=sys.stderr)

ans = [0] * n
for i, j in enumerate(res, 1):
    ans[j - 1] = i

print("!", *ans, flush=True)

