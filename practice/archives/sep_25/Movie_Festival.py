movies = []

for _ in range(int(input())):
    a,b = map(int,input().strip().split())
    movies.append((b,a))

movies.sort()
c = 0
last_end = 0

for b, a in movies:
    if a < last_end: continue 
    last_end = b
    c += 1

print(c)


