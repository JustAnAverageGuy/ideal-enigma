n,m = map(int,input().strip().split())

fixed = [ ]

allat = set("ABCD")

for _ in range(n):
    row = [*input()]
    mod = []
    if not fixed:
        for c in row:
            if not mod: mod.append( (allat - {c}).pop()); continue
            mod.append(
                (allat - {c, mod[-1]}).pop()
            )
        fixed.append(mod)
        print(*mod, sep="")
        continue

    for x,c in zip(fixed[-1],row):
        if not mod: mod.append( (allat - {c,x}).pop()); continue
        mod.append(
            (allat - {c, mod[-1], x}).pop()
        )
    print(*mod,sep="")
    fixed.append(mod)

