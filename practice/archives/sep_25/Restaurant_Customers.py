import sys

inp = sys.stdin.read().split()

events = []
typ = {}
for i in range(1, len(inp)):
    events.append(int(inp[i]))
    typ[int(inp[i])] = ((i&1)<<1)-1
    
events.sort()

mc = 0
c = 0
for e in events:
    c += typ[e]
    mc = max(c, mc)

print(mc)

