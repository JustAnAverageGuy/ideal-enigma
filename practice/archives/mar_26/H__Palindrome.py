n = int(input())

s = [input().strip() for _ in range(n)]
words = [
    "".join( s[i][j] if j < len(s[i]) else ""  for i in range(n)) 
    for j in range(n)
]

for word in words:
    print("YES" if word == word[::-1] else "NO")


