import sys;input=sys.stdin.readline

def solve():
    n = int(input())
    A = []
    for i in range(n):
        B = list(map(int,input().strip().split()))
        A.append(B)
    fst = dict()
    for i in A:
        fst[i[0]] = fst.get(i[0],0) + 1
    a0 = max(fst, key=lambda x: fst[x])
    for i in A:
        if i[0] != a0:
            print(a0, *i)
            return

for _ in range(int(input())): solve()

