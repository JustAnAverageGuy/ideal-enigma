import sys
input=sys.stdin.readline

def solve():
    n = int(input())
    s = list(map(int,input().strip()))
    ans = 0
    remover = [0]*(n+1)

    for i, b in enumerate(s, 1):
        if b == 0:
            for x in range(i, n+1, i):
                if s[x-1]: break 
                if not remover[x]: remover[x] = i

    ans = sum(remover)
    print(ans)

            
    
    

for _ in range(int(input())): solve()

