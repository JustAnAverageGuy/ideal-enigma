n = int(input())


A = []
for i in range(n):
    A.append(tuple(map(int,input().strip().split())))

bests = [[A[0][i]]  for i in range(3)]

for i in range(1,n):
    for j in range(3):
        bests[j].append(
            A[i][j] + max(bests[t][i-1] for t in range(3) if t != j)
        )


print(max(bests[i][n-1] for i in range(3)))


