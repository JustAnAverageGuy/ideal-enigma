import sys;input=sys.stdin.readline

def solve():
    n = int(input())
    A = list(map(int,input().strip().split()))
    i = 0
    while i < len(A)-1:
        if A[i] > A[i+1]:
            # split A[i]
            if A[i] < 10: return False
            A = A[:i] + [A[i]//10, A[i]%10] + A[i+1:]
            if i>0 and A[i-1] > A[i]: return False
        else: i += 1
    return True



for _ in range(int(input())): print("YES" if solve() else "NO")

