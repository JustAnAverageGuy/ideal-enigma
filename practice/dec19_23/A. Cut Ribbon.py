n,a,b,c = map(int,input().strip().split())

m = float('-inf')
for x in range(0, (n+a-1)//a + 1):
    for y in range(0, (n+b-1)//b + 1):
        cz = n - a*x - b*y
        if cz <= 0 or cz%c !=0: continue
        z = cz//c
        m = max(m ,x+y+z)

print(m)




















