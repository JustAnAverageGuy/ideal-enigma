from math import gcd
import sys

input = sys.stdin.readline


def resolv(a, b, x):
    a, b = sorted([a, b])
    if x in [a, b]: return True
    if x > b: return False
    if a == 0: return False
    if x > a:
        if (b - x) % a == 0: return True
    return resolv(b % a, a, x)


def solve():
    a, b, x = map(int, input().strip().split())
    if x % gcd(a, b) != 0: return False
    return resolv(a, b, x)


for _ in range(int(input())):
    print("YES" if solve() else "NO")
