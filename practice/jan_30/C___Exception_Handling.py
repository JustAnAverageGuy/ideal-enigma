n = int(input())
A = [int(input()) for i in range(n)]

pref = [0]
suff = [0]
for i in A: pref.append(max(pref[-1], i))

for i in A[::-1]: suff.append(max(suff[-1],i))

for j in range(1,n+1):
    print(max(pref[j-1], suff[n-j]))


