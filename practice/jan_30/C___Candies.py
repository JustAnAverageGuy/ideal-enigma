n = int(input())
A = list(map(int,input().strip().split()))
B = list(map(int,input().strip().split()))
for i in range(1,n): A[i] += A[i-1]
for j in range(n-2,-1,-1): B[j] += B[j+1]

print(max(A[k] + B[k]  for k in range(n)))


