a = [int(i) for i in input()]

def check(a,c):
    s = a[0]
    x = [str(a[0]),"","","","=7"]
    for i in range(3):
        s += a[i+1]*pow(-1, (c>>i)&1)
        x[i+1] = f"{'+-'[(c>>i)&1]}{a[i+1]}"
    if s == 7:
        print(*x,sep='')
        exit()
for c in range(8):
    check(a,c)
    


