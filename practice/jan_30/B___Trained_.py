n = int(input())
A = [0,*(int(input()) for _ in range(n))]
visited=set()
curr = 1
cnt = 0
while curr != 2:
    if curr in visited: print(-1); exit()
    visited.add(curr)
    cnt += 1
    curr = A[curr]
print(cnt)




