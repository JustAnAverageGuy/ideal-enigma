n,m = map(int,input().strip().split())
cans = []
for i in range(n):
    a,b = map(int,input().strip().split())
    cans.append((a,b))
cans.sort()
money = 0
cnt = 0
i = 0
while cnt < m:
    req = min(m-cnt, cans[i][1])
    cnt += req
    money += req*cans[i][0]
    # print(i, cnt, req, cans[i])
    i += 1
print(money)

