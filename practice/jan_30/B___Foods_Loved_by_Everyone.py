n,m = map(int,input().strip().split())
cnts = [0]*(m+1)
for i in range(n):
    A = list(map(int,input().strip().split()))
    for c in A[1:]: cnts[c] += 1

print(sum(cnts[i] == n for i in range(m+1)))

