n = int(input())
A = list(map(int,input().strip().split()))
B = list(map(int,input().strip().split()))
sm = 0
for i in range(n):
    m = min(A[i], B[i])
    A[i] -= m
    B[i] -= m
    sm += m
    m = min(A[i+1], B[i])
    A[i+1] -= m
    B[i] -= m
    sm += m
print(sm)


