import sys

input = sys.stdin.readline

n, m, k = map(int, input().strip().split())
A = [input().strip() for i in range(n)]

if k == 1:
    print(sum(i.count('.') for i in A))
    exit()

def get_arrangements(s, k):
    ans = 0
    currrun = 0
    for i in s:
        if i == '.': currrun += 1
        else:
            ans += max(0, currrun - k + 1)
            currrun = 0

    ans += max(0, currrun - k + 1)
    return ans


print(
    sum(get_arrangements(i, k) for i in A) +
    sum(get_arrangements((i[j] for i in A), k) for j in range(m))
)

