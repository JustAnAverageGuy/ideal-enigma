import sys;input=sys.stdin.readline

def solve():
    n = int(input())
    if n > 2020*2021+2020+2021: return True
    if n < 2020: return False
    b = n%2020
    if b == 0: return True
    for x in range(b, n//2021 + 1, b):
        a = (n - 2021*x)//2020
        if 2020*a+2021*x == n and a >= 0 and x >= 0 and a+x>0: return True
    return False

for _ in range(int(input())): print("YES" if solve() else "NO")

