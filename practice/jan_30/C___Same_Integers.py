a,b,c = map(int,input().strip().split())
a,b,c = sorted((a,b,c))

c1 = (c-a)//2
c2 = (c-b)//2

a += 2*c1
b += 2*c2
if a == b == c: 
    print(c1+c2)
elif a == b != c:
    print(c1+c2+1)
else:
    print(c1+c2+2)

