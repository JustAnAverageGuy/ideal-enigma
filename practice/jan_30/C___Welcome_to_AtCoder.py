n,m = map(int,input().strip().split())
is_ac = [False]*(n+1)
waed = [0]*(n+1)
tot_pent = 0
tot_ac = 0
for i in range(m):
    p,verd =  input().split()
    p = int(p)
    verd = (verd == "AC")
    if not is_ac[p]:
        if verd:
            is_ac[p] = True
            tot_ac += 1
            tot_pent += waed[p]
        else:
            waed[p] += 1
    else:
        continue
print(tot_ac, tot_pent)
