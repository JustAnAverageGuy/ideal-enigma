import sys;input=sys.stdin.readline

def solve():
    n = int(input())
    A = list(map(lambda x:  int(x)%2,input().strip().split()))
    lastodd = None 
    sm = 0
    for i,j in  enumerate(A):
        if j == 1:
            lastodd = i
        if lastodd is None: continue
        sm += lastodd + 1
    print(sm)

    

for _ in range(int(input())): solve()

