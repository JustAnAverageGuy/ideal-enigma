n = int(input())
A = list(map(int,input().strip().split()))
if len(set(A)) == 1:
    print(0)
    exit()
from math import gcd
# g = A[0]
# for i in A[1:]: g =gcd(i, g)

g = gcd(*A)

def extract(n):
    c = 0
    while  n%2 ==0:
        n >>= 1
        c += 1
    while  n%3 == 0:
        n //= 3
        c += 1
    if n != 1:
        print(-1)
        exit()
    return c

s = 0
for i in A:
    s += extract(i // g)
print(s)


