N=int(input())
A = list(map(int,input().strip().split()))

K = 30

X = [*A, 0]
sums = [0]*(N+1)

for k in range(K):
    one = [(x >> k) & 1 for x in X]
    count_set = sum(one)
    unset = N - count_set
    for i in range(N+1): sums[i] += (count_set - (count_set-unset)*one[i]) << k

ANS = max(sums)
print(ANS)

