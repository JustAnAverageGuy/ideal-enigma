n = int(input())
A = list(map(int, input().strip().split()))

A.reverse()

pos = [0] * n
for i in range(n):
    pos[A[i] - 1] = i

dp = [
    n,
]

for i in range(n, 0, -1):
    dp.append(min(dp[-1], pos[i - 1]))

dp.reverse()

s = 0
for i in range(n):
    if dp[i + 1] > i:
        s += i + 1
print(s)

