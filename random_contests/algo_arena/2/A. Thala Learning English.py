import sys;input=sys.stdin.readline

from collections import Counter
def solve():
    s = input().strip()
    c = Counter(s)
    return c["T"] > 0 and c["H"] > 0 and c["A"] > 1 and c["L"] > 0
    

for _ in range(int(input())): print("YES" if solve() else "NO")

