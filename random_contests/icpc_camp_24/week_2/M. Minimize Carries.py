def compute_carries(seq):
    s = 0
    tot = 0
    for i in seq:
        carr = 0
        t = s
        tb = i
        while (t > 0 and tb > 0) or (carr > 0):
            dt = t%10; t //= 10
            db = tb% 10; tb //= 10
            if dt +db +carr >= 10:
                carr = 1
                tot += 1
            else:
                carr = 0
        s += i
    return tot



