import sys;input=sys.stdin.readline

def solve():
    n,k = map(int,input().strip().split())
    odd = (n+1)//2
    x = n//k
    cx= k - n%k
    l = max((odd + x)//(x+1), (cx-k+odd+x-1)//(x))
    r = min((cx+odd)//(x+1), odd//x)
    return l <= r


for _ in range(int(input())): print("Yes" if solve() else "No")

