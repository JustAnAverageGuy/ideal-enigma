import sys;input=sys.stdin.readline

def solve():
    n = int(input())
    A = list(map(int,input().strip().split()))
    
    bit_counts = [0]*2

    k = 0
    while True:
        bit_counts = [0,0]
        for i in A: bit_counts[(i>>k)&1] += 1
        if all(bit_counts): print(1<<(k+1)); return
        k += 1

    


for _ in range(int(input())): solve()

